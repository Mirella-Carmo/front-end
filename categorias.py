class Categorias:
    def __init__(self):
        self.nome = ''
        self.imagem =''
    
    def set_nome(self, nome):
        self.nome = nome
    
    def get_nome(self):
        return self.nome
    
    def set_imagem(self, img):
        self.imagem = img
    
    def get_imagem(self):
        return self.imagem